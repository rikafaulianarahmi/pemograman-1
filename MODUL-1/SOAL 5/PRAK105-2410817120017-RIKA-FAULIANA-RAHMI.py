# Mendefinisikan variabel
a = 9
b = 5
x = 8
y = 8

# Menampilkan nilai variabel
print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel x bernilai {x}")
print(f"Variabel y bernilai {y}")

# Menghitung sisa bagi
sisa_bagi1 = a % b
sisa_bagi2 = x % y

# Menghitung total sisa bagi
total_sisa_bagi = sisa_bagi1 + sisa_bagi2

# Menampilkan total sisa bagi
print(f"Total sisa bagi dari a dibagi b dan x dibagi y adalah {total_sisa_bagi}")
