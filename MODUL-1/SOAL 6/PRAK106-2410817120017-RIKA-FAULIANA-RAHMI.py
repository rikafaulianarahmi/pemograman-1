# Mendefinisikan variabel
a = 4
b = 8
c = 3

# Menampilkan nilai variabel
print(f"Variabel a bernilai {a}")
print(f"Variabel b bernilai {b}")
print(f"Variabel c bernilai {c}")

# Melakukan pengecekan
print(f"Apakah a sama dengan b ? jawabannya adalah {1 if a == b else 0}")
print(f"Apakah b lebih besar dari c ? jawabannya adalah {1 if b > c else 0}")
print(f"Apakah a tidak sama dengan c ? jawabannya adalah {1 if a != c else 0}")
